package nju.software.jsonmodel;

import nju.software.entity.TeachingPoint;

public class JsonTeachingPoint {
	private Integer id;
	private String no;
	private String name;

	private String directorNo;
	private String directorName;
	private String location;
	
	public JsonTeachingPoint(TeachingPoint tp){
		this.setId(tp.getId());
		this.setNo(tp.getNo());
		this.setName(tp.getName());
		this.setDirectorNo(tp.getDirectorNo());
		this.setDirectorName(tp.getDirectorName());
		this.setLocation(tp.getLocation());
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDirectorNo() {
		return directorNo;
	}
	public void setDirectorNo(String directorNo) {
		this.directorNo = directorNo;
	}
	public String getDirectorName() {
		return directorName;
	}
	public void setDirectorName(String directorName) {
		this.directorName = directorName;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}

}
